// ─── COUNTDOWN ───────────────────────────────────────────────────────────────

function Countdown({ seconds, onDone }) {
  const [left, setLeft] = useState(seconds);
  useEffect(() => {
    if (left <= 0) { onDone?.(); return; }
    const t = setInterval(() => setLeft(l => l - 1), 1000);
    return () => clearInterval(t);
  }, [left]);
  const m = String(Math.floor(left / 60)).padStart(2, "0");
  const s = String(left % 60).padStart(2, "0");
  const pct = (left / seconds) * 100;
  const urgent = left <= 300;
  return (
    <div style={{
      background:"rgba(0,0,0,0.6)", backdropFilter:"blur(12px)",
      border:`2px solid ${urgent ? "#ff006e" : "#ffbe0b"}`,
      borderRadius:20, padding:"28px 40px", textAlign:"center",
      boxShadow:`0 0 40px ${urgent ? "#ff006e66" : "#ffbe0b44"}`,
      animation: urgent ? "urgentPulse 0.8s ease-in-out infinite alternate" : "none",
    }}>
      <div style={{ fontSize:13, fontFamily:"'Space Mono',monospace", color: urgent ? "#ff006e" : "#ffbe0b", letterSpacing:4, marginBottom:8 }}>
        {urgent ? "⚠ TIME RUNNING OUT" : "⏱ BUILD TIME REMAINING"}
      </div>
      <div style={{ fontSize:72, fontFamily:"'Space Mono',monospace", fontWeight:700, color: urgent ? "#ff006e" : "#fff", lineHeight:1, letterSpacing:-2 }}>
        {m}:{s}
      </div>
      <div style={{ marginTop:12, height:6, background:"#ffffff22", borderRadius:99, overflow:"hidden" }}>
        <div style={{
          width:`${pct}%`, height:"100%", borderRadius:99,
          background: urgent ? "linear-gradient(90deg,#ff006e,#ff4d6d)" : "linear-gradient(90deg,#ffbe0b,#06d6a0)",
          transition:"width 1s linear",
        }} />
      </div>
    </div>
  );
}

